    class Siswa {
        String nama;
        boolean absen;
        String id;

        public Siswa(String nama, boolean absen,String id) {
            this.nama = nama;
            this.absen = absen;
            this.id = id;
        }
    }

    public MainMenu() {
        initComponents();

        // Inisialisasi data siswa dan hitung absen
        daftarSiswa = new ArrayList<>();
        daftarSiswa.add(new Siswa("Budi", true, "1"));
        daftarSiswa.add(new Siswa("Ani", false, "2"));
        daftarSiswa.add(new Siswa("Tono", true, "3"));
        daftarSiswa.add(new Siswa("Sari", false, "4"));

        hitungJumlahAbsen();
    }

    private void hitungJumlahAbsen() {
        jumlahAbsen = 0;
        for (Siswa s : daftarSiswa) {
            if (s.absen) {
                jumlahAbsen++;
            }
        }
        
        lAbsensi.setText("" + jumlahAbsen);
       
    }
    
