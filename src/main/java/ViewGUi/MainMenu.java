    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
         DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
         int selectedRowIndex = jTable1.getSelectedRow();
                 
         tfNama.setText(model.getValueAt(selectedRowIndex, 0).toString());
         tfStatus.setText(model.getValueAt(selectedRowIndex, 1).toString());
         tfId.setText(model.getValueAt(selectedRowIndex, 2).toString());

        
    }//GEN-LAST:event_jTable1MouseClicked

    private void jTable1AncestorAdded(javax.swing.event.AncestorEvent evt) {//GEN-FIRST:event_jTable1AncestorAdded
        // TODO add your handling code here:
         DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        
        Object rowData[] = new Object[4];
        for(int i = 0; i < daftarSiswa.size(); i++)
        {
            rowData[0] = daftarSiswa.get(i).nama;
            rowData[1] = daftarSiswa.get(i).absen;
            rowData[2] = daftarSiswa.get(i).id;
            
            model.addRow(rowData);
        }
        
    }//GEN-LAST:event_jTable1AncestorAdded

