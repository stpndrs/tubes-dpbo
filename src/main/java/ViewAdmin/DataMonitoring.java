    private void tampilDataKeTabel() {
     DefaultTableModel model = (DefaultTableModel) tDataMonitoring.getModel();
    model.setRowCount(0); // reset isi tabel

    for (Data d : Data.getMonitoring()) {
        Object[] rowData = {
            d.getNama(),
            d.getNISN(),
            d.getKelas(),
            d.getMulai(),
            d.getInstansi()
        };
        model.addRow(rowData);
    }
}

